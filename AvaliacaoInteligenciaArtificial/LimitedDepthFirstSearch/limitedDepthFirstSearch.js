function ldfs(visited, graph, node, current, limit){
  if (!visited.includes(node) && current <= limit){
      RESULTS.push(node)
      visited.push(node)
      graph[node].forEach(function(neighbour){
          ldfs(visited, graph, neighbour, current + 1, limit)
      })
  }
}

